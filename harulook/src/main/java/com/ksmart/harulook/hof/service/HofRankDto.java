package com.ksmart.harulook.hof.service;

public class HofRankDto {

	private String boardNo;
	private int rank;
	private int liker;
	public String getBoardNo() {
		return boardNo;
	}
	public void setBoardNo(String boardNo) {
		this.boardNo = boardNo;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public int getLiker() {
		return liker;
	}
	public void setLiker(int liker) {
		this.liker = liker;
	}
	
	
}
