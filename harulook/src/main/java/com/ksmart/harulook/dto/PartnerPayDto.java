package com.ksmart.harulook.dto;

public class PartnerPayDto {

	private String cooPayNo;
	private String cooContractNo;
	private String cooPayDate;
	private int cooPayTotal;
	
	public String getCooPayNo() {
		return cooPayNo;
	}
	public void setCooPayNo(String cooPayNo) {
		this.cooPayNo = cooPayNo;
	}
	public String getCooContractNo() {
		return cooContractNo;
	}
	public void setCooContractNo(String cooContractNo) {
		this.cooContractNo = cooContractNo;
	}
	public String getCooPayDate() {
		return cooPayDate;
	}
	public void setCooPayDate(String cooPayDate) {
		this.cooPayDate = cooPayDate;
	}
	public int getCooPayTotal() {
		return cooPayTotal;
	}
	public void setCooPayTotal(int cooPayTotal) {
		this.cooPayTotal = cooPayTotal;
	}
	
	
}
